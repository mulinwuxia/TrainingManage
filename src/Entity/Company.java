package Entity;
/**
 * 培训公司类
 */
public class Company {

    private int id;            //培训公司编号
    private String name;       //培训公司名称
    private double money;      //培训公司资金

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getMoney() {
        return money;
    }

    public void setMoney(double money) {
        this.money = money;
    }



}
